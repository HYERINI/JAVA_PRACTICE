//try-catch 블록을 이용하여 정수를 0으로 나누는 경우
//"0으로 나눌 수 없습니다"라고 출력하고 다시 입력받는 프로그램

import java.util.Scanner;

public class DevideByZeroHandling {
	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		
		while(true) {
			System.out.print("나뉨수를 입력하세요 : ");
			int dividend = scanner.nextInt();  //나뉨수 입력
			
			System.out.print("나눗수를 입력하세요 : ");
			int divisor = scanner.nextInt();   //나눗수 입력
			
			//예외가 발생할 가능성이 있는 실행문
			try {
				System.out.println(dividend + "를 " + divisor + "로 나누면 몫은 " + dividend/divisor + "입니다.");
				break;   //정상적인 나누기 완료 후 while 벗어난다.
			}
			
			//예외처리문 (예외가 처리된 후에는 다시 try문으로 반복한다)	
			catch(ArithmeticException e) {
				System.out.println("0으로 나눌 수 없습니다! 다시 입력하세요.");
			}
		}
		
		scanner.close();
	}
}